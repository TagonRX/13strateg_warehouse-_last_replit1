const dataFilePath = config.paths.dataFile;
const data = JSON.parse(readFileSync(dataFilePath, "utf-8"));

// –ü–æ—Ä—è–¥–æ–∫ –∏–º–ø–æ—Ä—Ç–∞ (—Å —É—á–µ—Ç–æ–º –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π foreign keys)
const importOrder = [
  "users",
  "bulkUploadSources",
  "csvSources",
  "columnMappings",
  "pickingListColumnMappings",
  "warehouseSettings",
  "inventoryItems",
  "activeLocations",
  "pendingPlacements",
  "pendingTests",
  "pickingLists",
  "pickingListItems",
  "orders",
  "eventLogs",
  "workerAnalytics",
  "scheduledImports",
  "importHistory",
  "importFieldSettings"
];

async function importData() {
  let totalRecords = 0;
  let totalTables = 0;

  for (const tableName of importOrder) {
    const tableData = data[tableName];
    
    if (!tableData || tableData.length === 0) {
      console.log(`  ‚äò ${tableName.padEnd(30)} –ø—É—Å—Ç–æ`);
      continue;
    }

    try {
      // –ü–æ–ª—É—á–∞–µ–º —Ç–∞–±–ª–∏—Ü—É –∏–∑ —Å—Ö–µ–º—ã
      const table = (schema as any)[tableName];
      
      if (!table) {
        console.log(`  ‚úó ${tableName.padEnd(30)} —Ç–∞–±–ª–∏—Ü–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞ –≤ —Å—Ö–µ–º–µ`);
        continue;
      }

      // –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –¥–∞–Ω–Ω—ã–µ
      await db.insert(table).values(tableData);
      
      totalRecords += tableData.length;
      totalTables++;
      
      console.log(`  ‚úì ${tableName.padEnd(30)} ${tableData.length.toLocaleString()} –∑–∞–ø–∏—Å–µ–π`);
    } catch (error: any) {
      console.log(`  ‚úó ${tableName.padEnd(30)} –û–®–ò–ë–ö–ê: ${error.message}`);
    }
  }

  console.log("\n‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
  console.log(`‚úÖ –ò–ú–ü–û–†–¢ –ó–ê–í–ï–†–®–ï–ù!`);
  console.log(`   –¢–∞–±–ª–∏—Ü –æ–±—Ä–∞–±–æ—Ç–∞–Ω–æ: ${totalTables}`);
  console.log(`   –ó–∞–ø–∏—Å–µ–π –∑–∞–≥—Ä—É–∂–µ–Ω–æ: ${totalRecords.toLocaleString()}`);
  console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n");
}

